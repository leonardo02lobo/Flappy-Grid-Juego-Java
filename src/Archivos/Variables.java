package Archivos;

public class Variables {
    
    public String getInfomacion() {
        return Infomacion;
    }

    public void setInfomacion(String Infomacion) {
        this.Infomacion = Infomacion;
    }

    public String getAceptar() {
        return aceptar;
    }

    public void setAceptar(String aceptar) {
        this.aceptar = aceptar;
    }

    public String getCancelar() {
        return cancelar;
    }

    public void setCancelar(String cancelar) {
        this.cancelar = cancelar;
    }

    public String getConfiguracion() {
        return configuracion;
    }

    public void setConfiguracion(String configuracion) {
        this.configuracion = configuracion;
    }

    public String getIdioma() {
        return idioma;
    }

    public void setIdioma(String idioma) {
        this.idioma = idioma;
    }

    public String getPuntaje() {
        return puntaje;
    }

    public void setPuntaje(String puntaje) {
        this.puntaje = puntaje;
    }

    private String Infomacion;
    private String aceptar;
    private String cancelar;
    private String configuracion;
    private String idioma;
    private String puntaje;
}
